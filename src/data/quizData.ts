export const calculateType = (answers: string[], gender: 'female' | 'male'): string => {
  let D = 0, S = 0, T = 0, E = 0;
  
  answers.forEach((answer) => {
    if (answer === 'D') D++;
    if (answer === 'S') S++;
    if (answer === 'T') T++;
    if (answer === 'E') E++;
  });
  
  // Determine primary axes
  const teAxis = T >= E ? 'T' : 'E';
  const dsAxis = D >= S ? 'D' : 'S';
  
  // Determine dominant (which one has bigger difference from center)
  const teDiff = Math.abs(T - E);
  const dsDiff = Math.abs(D - S);
  
  let dominant: string;
  if (teDiff >= dsDiff) {
    dominant = teAxis;
  } else {
    dominant = dsAxis;
  }
  
  return `${teAxis}${dsAxis}_${dominant}`;
};

export const calculateCoordinates = (answers: string[]): { x: number; y: number } => {
  let D = 0, S = 0, T = 0, E = 0;
  
  answers.forEach((answer) => {
    if (answer === 'D') D++;
    if (answer === 'S') S++;
    if (answer === 'T') T++;
    if (answer === 'E') E++;
  });
  
  // x: D-S axis (-5 to 5), y: T-E axis (-5 to 5)
  const x = ((D - S) / 5) * 50;
  const y = ((T - E) / 5) * 50;
  
  return { x, y };
};

export const getTypeData = (typeCode: string, gender: 'female' | 'male'): HormoneType => {
  const types = gender === 'female' ? femaleTypes : maleTypes;
  return types[typeCode] || femaleTypes['TD_T'];
};
